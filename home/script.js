var count = 0;

        //clothes-gallery (sweater)//
function changeLoveIconColor1(x){
    var x;
    if(x == 1){
        count = count + 1;
    }

    if(count == 1){
        document.getElementById("love-icon-sweater").style.color="red";
    }
    else if(count == 2){
        document.getElementById("love-icon-sweater").style.color="white";
        count = 0;
    }

}

